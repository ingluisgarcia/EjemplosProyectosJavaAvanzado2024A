    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        if (!txtDocumento.getText().isEmpty() && 
                !txtNombre.getText().isEmpty()
                && !txtEdad.getText().isEmpty() 
                && comboProgramas.getSelectedIndex() > 0) {
            documento = txtDocumento.getText();
            if (listaEstudiante.isEmpty()) {
                nombre = txtNombre.getText().toUpperCase();
                edad = txtEdad.getText();
                programa = programas[comboProgramas.getSelectedIndex()-1];
                listaEstudiante.add(new Estudiante(documento, nombre, 
                        edad, programa));
                JOptionPane.showMessageDialog(rootPane, 
                        "Dato agregado con exito");
                mostrarInfo();
            } else {
                int pos = -1;
                for (int i = 0; i < listaEstudiante.size(); i++) {
                    if (listaEstudiante.get(i).getDocumento().equals(documento)) {
                        pos = i;
                        break;
                    }
                }
                if (pos == -1) {
                    nombre = txtNombre.getText().toUpperCase();
                    edad = txtEdad.getText();
                    programa = programas[comboProgramas.getSelectedIndex() - 1];
                    listaEstudiante.add(new Estudiante(documento, nombre, edad, programa));
                    JOptionPane.showMessageDialog(rootPane, "Dato agregado con exito");
                    mostrarInfo();
                } else {
                    JOptionPane.showMessageDialog(rootPane, "El estudiante ya ha sido agregado");
                }
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Faltan campos por diligenciar");
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

    public void llenarCombo() {
        comboProgramas.removeAllItems();
        comboProgramas.addItem("Seleccione...");
        for (int i = 0; i < programas.length; i++) {
            comboProgramas.addItem(programas[i]);
        }
    }
    
    public void mostrarInfo(){
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.setColumnIdentifiers(encabezado);
        for(int i=0;i<listaEstudiante.size();i++){
            modelo.addRow(new Object[]{
                listaEstudiante.get(i).getDocumento(),
                listaEstudiante.get(i).getNombre(),
                listaEstudiante.get(i).getEdad(),
                listaEstudiante.get(i).getPrograma()});
        }
        tableEstudiantesRegistrados.setModel(modelo);
    }

